// Assignment
let a = 10;
let b = 5;

// addition
let sum = (a += b);
console.log("addition is", sum);

// subtraction
let sub = (a -= b);
console.log("sub is", sub);

// multiplication
let multi = (a *= b);
console.log("multiplication is", multi);

// division
let division = (a /= b);
console.log("division is", division);

// exponential
let expo = (a **= b);
console.log(expo);

// modular
let mod = 10 % 3;
console.log(mod);