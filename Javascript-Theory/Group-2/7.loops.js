// For loop
for (let i = 0; i <= 5; i++) {
  console.log("The number is " + i);
}

for (let i = 5; i >= 1; i--) {
  console.log("The Reverse number is " + i);
}

// While loop
let j = 0;
while (j < 5) {
  console.log("The number is " + j);
  j++;
}

// Do While loop
let k = 6;
do {
  console.log(k);
  k++;
} while (k < 5);

